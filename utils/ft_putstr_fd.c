#include "../cub.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (!s)
	{
		ft_putstr_fd("(null)", 1);
		return ;
	}	
	while (*s)
	{
		ft_putchar_fd(*s, fd);
		s++;
	}
}
